public class Matress {
    private int length;
    private int width;
    private int thickness;
    private String Manufacturer;
    private String Model;

    public Matress(int length, int width, int thickness, String manufacturer, String model) {
        this.length = length;
        this.width = width;
        this.thickness = thickness;
        Manufacturer = manufacturer;
        Model = model;
    }
}
